package datatypes;

public class Literals {
	public static void main(String[] args) {
		
		byte b1 = 10;
		byte b2 = 0b1010;
		byte b3 = 012;
		byte b4 = 0XA;
		
		System.out.println(b1);//10
		System.out.println(b2);//10
		System.out.println(b3);//10
		System.out.println(b4);//10
	}
}
